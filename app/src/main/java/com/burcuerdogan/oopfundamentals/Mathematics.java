package com.burcuerdogan.oopfundamentals;

public class Mathematics {

    //Static Polymorphism (3 Methodun da adı aynı ama yapacağı işlemler farklı)

    public int sum(){
        return 0;
    }

    public int sum(int x, int y){
        return x + y;
    }

    public int sum(int x, int y, int z){
        return x + y + z;
    }

}
